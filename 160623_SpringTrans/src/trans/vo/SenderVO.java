package trans.vo;

public class SenderVO {
	private String send_num;
	private String send_customer;
	long amount;
	
	
	public String getSend_num() {
		return send_num;
	}
	public void setSend_num(String send_num) {
		this.send_num = send_num;
	}
	public String getSend_customer() {
		return send_customer;
	}
	public void setSend_customer(String send_customer) {
		this.send_customer = send_customer;
	}
	public long getAmount() {
		return amount;
	}
	public void setAmount(long amount) {
		this.amount = amount;
	}
	
	
	
	
}
